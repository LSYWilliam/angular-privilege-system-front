export class CityLinkModel {
  /**省*/
  private _province: any;
  /**市*/
  private _city: any;
  /**区*/
  private _area: any;
  /**详细地址*/
  private _address: any;
  /**省code码*/
  private _provinceCode: any;
  /**市code码*/
  private _cityCode: any;
  /**区code码*/
  private _areaCode: any;
  /**获取具体的省*/
  get province(): any {
    return this._province;
  }
  /**设置具体的省*/
  set province(value: any) {
    this._province = value;
  }
  /**获取具体的市*/
  get city(): any {
    return this._city;
  }
  /**设置具体的省*/
  set city(value: any) {
    this._city = value;
  }
  /**获取具体的区*/
  get area(): any {
    return this._area;
  }
  /**设置具体的区*/
  set area(value: any) {
    this._area = value;
  }
  /**获取具体的地址*/
  get address(): any {
    return this._address;
  }
  /**设置具体的地址*/
  set address(value: any) {
    this._address = value;
  }

  /**获取省Code*/
  get provinceCode(): any {
    return this._provinceCode;
  }
  /**获取省Code*/
  set provinceCode(value: any) {
    this._provinceCode = value;
  }

  /**获取市Code*/
  get cityCode(): any {
    return this._cityCode;
  }
  /**设置市Code*/
  set cityCode(value: any) {
    this._cityCode = value;
  }

  /**获取区Code*/
  get areaCode(): any {
    return this._areaCode;
  }
  /**设置区Code*/
  set areaCode(value: any) {
    this._areaCode = value;
  }
}


